Siga Comunicaciones 
¿Quiénes somos? Somos un grupo de periodistas y comunicadores de varias generaciones dedicados a la constante búsqueda de la transformación de la sociedad por medio de la transmisión y manejo de la información de interés público.
Fundadores y colaboradores: 
Paulina Lemus Hernández
Lic. Luis Eduardo Torres Lazcano 
Mtro. Eduardo Garduño Campa
Mtro. Carlos Hugo González Calderón
Escritor: Eduardo Osorio García
Misión: Mantener a la sociedad informada sobre el acontecer diario y formar la opinión pública por medio de las noticias, reportajes y voces de especialistas con un alto nivel de calidad en todos nuestros contenidos.
Visión: Ser uno de los medios digitales con mayor influencia en la opinión pública de la región y consolidarnos como un semillero de nuevos talentos en el gremio periodístico.
Valores: Responsabilidad, Integridad, Transparencia  e Inclusión 
Slogan: Periodismo con transparencia e inclusión social

